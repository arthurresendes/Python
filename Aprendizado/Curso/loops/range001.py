#For de 0 a 10
for i in range(11):
    print(i)
    
print('\n')

#For de 1 ate 10
for j in range(1,11):
    print(j)

print('\n')

#for comeca no 1 vai ate 10 pulando de dois em dois
for x in range(1,11,2):
    print(x)
    
print('\n')

#Loop ao contrario
for y in range(10 , 0 ,-1):
    print(y)
    
